function credentialInventory({ answers, auth, secrets, db, name, teammates }) {
  const rows = [];
  const owner = teammates[0]?.name || 'TBD';
  const storage = secrets?.id === 'doppler' ? 'Doppler' : '.env (local)';
  if (auth?.id === 'okta-oidc' || auth?.id === 'okta-device') {
    rows.push(`| OIDC client | OKTA_CLIENT_ID | ${owner} | No rotation (app-scoped) | ${storage} |`);
    rows.push(`| OIDC issuer URL | OKTA_ISSUER | ${owner} | Never | ${storage} |`);
  }
  if (auth?.id === 'okta-client-creds') {
    rows.push(`| Client credentials | OKTA_CLIENT_ID / OKTA_CLIENT_SECRET | ${owner} | 90 days | ${storage} |`);
  }
  if (db?.id && db.id !== 'none') {
    rows.push(`| Database URL | DATABASE_URL | ${owner} | Password: 90 days | ${storage} |`);
  }
  answers.integration.externalApis.forEach(api => {
    rows.push(`| API token | ${envVarName(api)}_TOKEN | ${owner} | 90 days (or vendor default) | ${storage} |`);
  });
  rows.push(`| Deploy SSH key | DEPLOY_SSH_KEY | DevEx | 1 year | ${storage} |`);
  return rows.join('\n');
}

