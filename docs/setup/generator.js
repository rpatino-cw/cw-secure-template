function projectSpecificRules({ answers, lang, arch, db, cls, auth, secrets }) {
  const rules = [];
  if (auth?.id === 'okta-oidc') {
    rules.push('- **Auth:** Every endpoint except `/healthz` uses `Depends(get_current_user)` (Python) or `middleware.RequireAuth()` (Go). Group claims drive RBAC.');
  } else if (auth?.id === 'okta-client-creds') {
    rules.push('- **Auth:** Service-to-service only. Validate client ID + JWT signature. Reject any request without both.');
  } else if (auth?.id === 'okta-device') {
    rules.push('- **Auth:** CLI tool using Device Authorization Grant. Token stored in OS keychain, never in plain file.');
  } else if (auth?.id === 'none') {
    rules.push('- **Auth:** Explicitly disabled by wizard choice. AppSec must approve before deploy. Document justification in this CLAUDE.md.');
  }

  if (secrets?.id === 'doppler') {
    rules.push('- **Secrets:** Doppler project `' + answers.project.name + '` with configs `dev` / `stg` / `prd`. Local dev uses `.env` (gitignored).');
    rules.push('- **Never commit `.env`.** Pre-commit blocks this. No workaround.');
  } else if (secrets?.id === 'env') {
    rules.push('- **Secrets:** Plain `.env` only (development posture). Before production: migrate to Doppler.');
  }

  if (db?.id && db.id !== 'none') {
    rules.push(`- **Database:** ${db.name}. All queries parameterized. Never \`SELECT *\`. Migrations in \`/migrations\`, numbered sequentially.`);
    if (db.id === 'sqlite') rules.push('- **SQLite warning:** Single-writer. Not for multi-user production. Migrate before scale.');
    if (db.id === 'mongodb') rules.push('- **MongoDB:** Requires AppSec review before production. Use transactions explicitly when needed.');
  }

  if (answers.integration.api === 'rest') {
    rules.push('- **API shape:** REST. Resource-based URLs. Standard HTTP verbs. Response format: flat JSON, no wrapper.');
  } else if (answers.integration.api === 'graphql') {
    rules.push('- **API shape:** GraphQL. Schema in `graphql/schema.graphql`. Resolvers in `graphql/resolvers/`. Every resolver goes through a service — never direct to repo.');
  } else if (answers.integration.api === 'grpc') {
    rules.push('- **API shape:** gRPC. `.proto` files in `proto/`. Generated stubs in `gen/`. Internal-only, no public exposure.');
  }

  if (answers.integration.queue !== 'none') {
    rules.push(`- **Queue:** ${QUEUE_SYSTEMS.find(q => q.id === answers.integration.queue)?.label}. Handlers in \`tasks/\` (Python) or \`internal/handlers/\` (Go). Every handler must be idempotent.`);
  }

  if (answers.integration.externalApis.length > 0) {
    rules.push(`- **External APIs:** ${answers.integration.externalApis.join(', ')}. All outbound calls go through \`services/external/\`. Never call third-party APIs directly from a route.`);
  }

  const tier = SCALE_TIERS.find(t => t.id === answers.scale.users);
  if (tier?.id === 'tier-3' || tier?.id === 'tier-4') {
    rules.push(`- **Scale:** ${tier.label}. All writes must be idempotent. Assume any handler can be called twice.`);
  }

  if (answers.scale.pattern === 'spiky') {
    rules.push('- **Traffic:** Spiky. Queue buffers must cap. Circuit breakers on every external call. No unbounded fan-out.');
  }

  return rules.join('\n');
}

