// generator.js — composes a tailored scaffold zip from answers + template files.
// Uses JSZip (loaded via CDN in setup.html).

import { SCALE_TIERS, QUEUE_SYSTEMS, resolveAnswers } from './stacks.js';

export const slugify = (s) => String(s || '').toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
export const envVarName = (s) => String(s || '').toUpperCase().replace(/[^A-Z0-9]/g, '_');
const escapeHtml = (s) => String(s ?? '').replace(/[&<>"']/g, c => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' })[c]);
// Accept owns as either legacy string or new array.
const ownsList = (owns) => Array.isArray(owns) ? owns.filter(Boolean) : (owns ? String(owns).split(',').map(s => s.trim()).filter(Boolean) : []);
const ownsLabel = (owns) => { const list = ownsList(owns); return list.length ? list.join(', ') : 'shared'; };

export async function generateScaffold(answers) {
  const zip = new JSZip();
  const name = answers.project.name || 'my-new-service';
  const root = zip.folder(name);

  const today = new Date().toISOString().slice(0, 10);
  const resolved = resolveAnswers(answers);
  const { lang, arch, db, cls, auth, secrets, deploy, theme, teammates } = resolved;
  const ctx = { answers, ...resolved, name, today };

  // Core generated files
  root.file('README.md', readme(ctx));
  root.file('CLAUDE.md', claudeMd(ctx));
  root.file('SECURITY-REPORT.md', securityReport(ctx));
  root.file('.env.example', envExample(ctx));
  root.file('.gitignore', gitignore(ctx));
  root.file('.stack', lang.id);
  root.file('.framework-version', 'cw-secure-v2\n');
  root.file('Makefile', makefile(ctx));
  root.file('docker-compose.yml', dockerCompose(ctx));

  // .claude config
  const claudeDir = root.folder('.claude');
  claudeDir.file('settings.local.json', settingsLocal(ctx));
  claudeDir.file('MEMORY.md', projectMemory(ctx));

  // Pre-commit + CI
  root.file('.pre-commit-config.yaml', preCommitConfig(ctx));
  root.folder('.github').folder('workflows').file('ci.yml', ciYaml(ctx));

  // rooms.json (only if team > 1)
  if (teammates.length > 1) {
    root.file('rooms.json', roomsJson(ctx));
  }

  // Language scaffold
  if (lang.id === 'python') {
    await addPythonScaffold(root, ctx);
  } else if (lang.id === 'node') {
    await addNodeScaffold(root, ctx);
  } else {
    await addGoScaffold(root, ctx);
  }

  // Sub-architecture style folders (hexagonal, clean-arch, DDD, MVC, etc.)
  addSubstyleFolders(root, ctx);

  // Outbound API egress pipeline (conditional)
  if (answers.integration.outboundPipeline?.enabled) {
    addOutboundPipeline(root, ctx);
  }

  // Dashboard
  if (answers.theme.includeDashboard) {
    root.file('project-dashboard.html', projectDashboard(ctx));
  }

  // Trigger download
  const blob = await zip.generateAsync({ type: 'blob', compression: 'DEFLATE', compressionOptions: { level: 6 } });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = `${name}-scaffold.zip`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

