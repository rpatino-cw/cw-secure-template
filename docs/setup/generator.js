function goRateLimitMiddleware(ctx) {
  return `package middleware

import (
	"net/http"
	"sync"
	"time"
)

type rateLimiter struct {
	mu      sync.Mutex
	buckets map[string][]time.Time
	rpm     int
}

func RateLimit(next http.Handler, rpm int) http.Handler {
	rl := &rateLimiter{buckets: map[string][]time.Time{}, rpm: rpm}
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		if rl.allow(r.RemoteAddr) {
			next.ServeHTTP(w, r)
			return
		}
		w.Header().Set("Retry-After", "60")
		http.Error(w, \`{"error":"rate limit exceeded"}\`, http.StatusTooManyRequests)
	})
}

func (rl *rateLimiter) allow(ip string) bool {
	rl.mu.Lock()
	defer rl.mu.Unlock()
	now := time.Now()
	kept := rl.buckets[ip][:0]
	for _, t := range rl.buckets[ip] {
		if now.Sub(t) < time.Minute {
			kept = append(kept, t)
		}
	}
	if len(kept) >= rl.rpm {
		rl.buckets[ip] = kept
		return false
	}
	rl.buckets[ip] = append(kept, now)
	return true
}
`;
}

