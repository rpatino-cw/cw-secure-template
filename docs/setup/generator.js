function statusIcon(s) { return { pass: '✅', warn: '⚠️', fail: '❌' }[s] || '—'; }

function cwPolicyChecklist({ answers, cls, auth, secrets, deploy }) {
  const p = [];
  // SDLC
  p.push({ policy: 'SDLC Policy', status: 'pass', detail: 'Pre-commit hooks, 80% coverage gate, PR template, CI scans — all wired in this scaffold.' });
  // Data Classification
  if (!cls) p.push({ policy: 'Data Classification', status: 'fail', detail: 'No classification set.' });
  else p.push({ policy: 'Data Classification', status: 'pass', detail: `Classified as ${cls.label}. ${cls.strict ? 'Strict guards enabled.' : 'Standard guards active.'}` });
  // Access Management
  if (auth?.id === 'none') p.push({ policy: 'Access Management', status: 'fail', detail: 'No auth configured. AppSec cannot approve.' });
  else if (auth?.id.startsWith('okta')) p.push({ policy: 'Access Management', status: 'pass', detail: `Okta OIDC — ${auth.label}.` });
  // Encryption & Key Management
  if (secrets?.id === 'doppler') p.push({ policy: 'Encryption and Key Management', status: 'pass', detail: 'Doppler + ESO — keys never touch code or git.' });
  else if (secrets?.id === 'env' && cls?.strict) p.push({ policy: 'Encryption and Key Management', status: 'fail', detail: 'Plain .env is not acceptable for Restricted data. Switch to Doppler.' });
  else p.push({ policy: 'Encryption and Key Management', status: 'warn', detail: 'Plain .env OK for development, not production.' });
  // Vulnerability Management
  p.push({ policy: 'Vulnerability Management', status: 'pass', detail: 'CodeQL, gosec/bandit, Dependabot all enabled in CI.' });
  // Change Management
  p.push({ policy: 'Change Management', status: 'pass', detail: 'Branch protection + required PRs + CODEOWNERS enforced.' });
  // Incident Management
  p.push({ policy: 'Security Incident Management', status: 'pass', detail: 'SECURITY.md includes /inci trigger + contact escalation.' });
  // Audit Log
  if (cls?.strict || answers.security.compliance.includes('soc2')) {
    p.push({ policy: 'Audit Log Management', status: 'pass', detail: 'Structured JSON logs + auth event tracking — tests enforce presence.' });
  } else {
    p.push({ policy: 'Audit Log Management', status: 'pass', detail: 'Structured JSON logs. Auth events logged.' });
  }
  // OSS Policy
  p.push({ policy: 'OSS Policy', status: 'pass', detail: 'Dependencies pinned in ' + (answers.stack.language === 'python' ? 'pyproject.toml' : 'go.mod') + '. SBOM generated in CI.' });
  // Password Management
  if (auth?.id.startsWith('okta')) p.push({ policy: 'Password Management', status: 'pass', detail: 'No passwords stored — Okta owns auth.' });
  // Third-Party Risk
  if (answers.integration.externalApis.length > 0) {
    p.push({ policy: 'Third-Party Risk Management', status: 'warn', detail: `External APIs: ${answers.integration.externalApis.join(', ')}. Each needs a TPRM review before production use.` });
  }
  return p;
}

