function nextActions({ answers, cls, auth, secrets, deploy }) {
  const actions = [];
  if (auth?.id.startsWith('okta')) actions.push('1. File Freshservice ticket for Okta app registration. Include app name, redirect URIs, required groups.');
  if (secrets?.id === 'doppler') actions.push('2. Create Doppler project matching your app name. Add `OKTA_CLIENT_ID`, `OKTA_ISSUER`, `DATABASE_URL` to dev config first.');
  if (cls?.strict) actions.push('3. Open AppSec review in `#application-security` at least 2 weeks before launch. Include this SECURITY-REPORT.md in the ticket.');
  if (deploy?.id?.startsWith('core')) actions.push('4. Request cluster namespace + Helm deploy credentials from DevEx.');
  actions.push(`${actions.length + 1}. Run \`./setup.sh && make doctor\` to verify the scaffold builds cleanly.`);
  actions.push(`${actions.length + 1}. Run \`make check\` before every commit. This is the same gate that blocks CI — run it locally first.`);
  return actions.map(a => `- [ ] ${a.replace(/^\d+\. /, '')}`).join('\n');
}

